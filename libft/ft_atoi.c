long	ft_atoi(const char *str)
{
	unsigned int	i;
	long			n;
	long			sign;

	n = 0;
	sign = 1;
	i = 0;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n *= 10;
		n += *(str + i) - '0';
		str++;
	}
	return (sign * n);
}
